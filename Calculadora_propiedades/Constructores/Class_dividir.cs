namespace Calculadora_propiedades
{
    internal class Class_dividir
    {
        double can1, can2;
        public Class_dividir(double can1, double can2)
        {
            this.can2 = can2;
            this.can1 = can1;
        }
        public double dividir()
        {
            return can1 / can2;
        }
    }
}